#!/usr/bin/env node
/**
 * Generate a SQL file that "stamps" (marks as applied) D1 migrations by inserting
 * filenames into the `d1_migrations` table.
 *
 * Why: If a DB was created historically via `wrangler d1 execute --file ...`,
 * Wrangler may think all migration files are unapplied. Stamping lets us adopt
 * `wrangler d1 migrations apply` safely (after verifying schema matches).
 *
 * This script is intentionally offline: it does NOT talk to Cloudflare.
 * It only produces a SQL file you can run with:
 *   wrangler d1 execute <DB> --remote --file <out.sql>
 */

import fs from "node:fs";
import path from "node:path";
import process from "node:process";

function parseArgs(argv) {
  const args = {
    migrationsDir: null,
    out: null,
    upTo: null,
    include: [],
    exclude: [
      "0001_initial_schema.sql",
      "0002_seed_data.sql",
      "0003_hybrid_mode.sql",
    ],
  };

  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--migrations-dir") args.migrationsDir = argv[++i];
    else if (a === "--out") args.out = argv[++i];
    else if (a === "--up-to") args.upTo = argv[++i];
    else if (a === "--include") args.include.push(argv[++i]);
    else if (a === "--exclude") args.exclude.push(argv[++i]);
    else if (a === "--help" || a === "-h") args.help = true;
    else throw new Error(`Unknown arg: ${a}`);
  }
  return args;
}

function usage() {
  return `Usage:
  node scripts/generate-d1-stamp-sql.mjs --migrations-dir <dir> --up-to <filename> --out <out.sql>

Options:
  --migrations-dir  Path to migrations folder (e.g. packages/database/src/migrations)
  --up-to           Include migrations up to and including this filename (lexicographic order)
  --out             Output SQL file path
  --exclude         Filename to exclude (repeatable). Defaults exclude bootstrap seed files.
  --include         Only include filenames that contain this substring (repeatable)

Example:
  node scripts/generate-d1-stamp-sql.mjs \\
    --migrations-dir packages/database/src/migrations \\
    --up-to 0077_create_store_users_shim.sql \\
    --out tmp/stamp-up-to-0077.sql
`;
}

function escapeSqlString(s) {
  return s.replaceAll("'", "''");
}

const args = parseArgs(process.argv);
if (args.help) {
  process.stdout.write(usage());
  process.exit(0);
}

if (!args.migrationsDir || !args.upTo || !args.out) {
  process.stderr.write(usage());
  process.exit(1);
}

const migrationsDirAbs = path.resolve(process.cwd(), args.migrationsDir);
const outAbs = path.resolve(process.cwd(), args.out);

if (!fs.existsSync(migrationsDirAbs)) {
  throw new Error(`migrations dir not found: ${migrationsDirAbs}`);
}

const entries = fs.readdirSync(migrationsDirAbs, { withFileTypes: true });
let files = entries
  .filter((e) => e.isFile())
  .map((e) => e.name)
  .filter((n) => n.toLowerCase().endsWith(".sql"))
  .filter((n) => !args.exclude.includes(n));

if (args.include.length) {
  files = files.filter((n) => args.include.some((needle) => n.includes(needle)));
}

files.sort((a, b) => a.localeCompare(b));

const upToIndex = files.indexOf(args.upTo);
if (upToIndex === -1) {
  throw new Error(
    `--up-to file not found after filtering: ${args.upTo}\n` +
      `Hint: check excludes/includes. First few files: ${files.slice(0, 10).join(", ")}`
  );
}

const selected = files.slice(0, upToIndex + 1);

fs.mkdirSync(path.dirname(outAbs), { recursive: true });

const sql = [
  "-- Generated by scripts/generate-d1-stamp-sql.mjs",
  "-- Inserts migration filenames into d1_migrations (idempotent).",
  "",
  "BEGIN;",
  ...selected.map(
    (name) =>
      `INSERT OR IGNORE INTO d1_migrations(name) VALUES ('${escapeSqlString(name)}');`
  ),
  "COMMIT;",
  "",
].join("\n");

fs.writeFileSync(outAbs, sql, "utf8");

process.stdout.write(
  `Wrote ${selected.length} stamps to ${path.relative(process.cwd(), outAbs)}\n`
);
